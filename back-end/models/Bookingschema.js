const mongoose = require("mongoose");
const Bookingschema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  state: {
    type: String,
    required: true,
  },
  pincode: {
    type: String,
    required: true,
  },
  date: {
    type: String,
    required: true,
  },
});
const Data = mongoose.model("Booking_DB", Bookingschema);
module.exports = Data;
